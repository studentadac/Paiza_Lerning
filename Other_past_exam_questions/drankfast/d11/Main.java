import java.util.*;
public class Main {
    public static void main(String[] args) {
        // 自分の得意な言語で
        // Let's チャレンジ！！
        Scanner sc = new Scanner(System.in);
        var line = sc.next();
        for (int i=0;i<line.length() ;i++ )
        {
            System.out.println(line.charAt(i));
        }
        
    }
}
