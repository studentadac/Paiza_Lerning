import java.util.*;
public class Main {
    public static void main(String[] args) {
        // 自分の得意な言語で
        // Let's チャレンジ！！
        Scanner sc = new Scanner(System.in);
        int line = sc.nextInt();
        for (int i=0;i<line ;i++ )
        {
            System.out.println("#".repeat(10));
            System.out.println(".".repeat(10));
        }
        
    }
}
