import java.util.*;
public class Main {
    public static void main(String[] args) {
        // 自分の得意な言語で
        // Let's チャレンジ！！
        Scanner sc = new Scanner(System.in);
        var line = sc.nextLine().split(" ");//一行を" "で分割したサイズ2の文字列配列line
        var s=String.join("",line);//文字列配列を空文字列で結合した文字列s
        
        System.out.println(s);
        
      
    }
}
