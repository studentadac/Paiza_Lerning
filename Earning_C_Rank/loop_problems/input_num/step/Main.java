import java.util.*;
public class Main {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        for (int i=0;i<10 ;i++ )
        {
            int n = sc.nextInt();
            System.out.println(n);
        }
        sc.close();
    }
}
