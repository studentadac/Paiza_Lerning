public class Main {
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        final int n=sc.nextInt(),k=sc.nextInt();
        sc.close();
        
        for (int i=0;i<k ;i++ )
        {
            System.out.println(n);
        }
    }
}
